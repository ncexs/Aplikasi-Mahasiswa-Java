    private void BTsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTsimpanActionPerformed
        // TODO add your handling code here:
        try {
            String sql = "INSERT INTO mahasiswa (nim, nama, alamat, kota) VALUES (?, ?, ?, ?)";
            PreparedStatement pst = conn.prepareStatement(sql);
            
            // Ambil data dari kotak input
            pst.setString(1, TFnim.getText());
            pst.setString(2, TFnama.getText());
            pst.setString(3, TFalamat.getText());
            pst.setString(4, TFkota.getText());
            
            pst.executeUpdate(); // Jalankan perintah simpan
            
            JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan!");
            
            tampilkanData(); // Refresh tabel agar data baru muncul
            bersihkanForm(); // Kosongkan form
            
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal Simpan: " + e.getMessage());
        }
    }//GEN-LAST:event_BTsimpanActionPerformed

    private void BThapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BThapusActionPerformed
        // TODO add your handling code here:
        try {
        String nim = TFnim.getText();
        
        if (nim.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pilih data yang akan dihapus dari tabel.", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Konfirmasi Hapus
        int konfirmasi = JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus data NIM: " + nim + "?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
        
        if (konfirmasi == JOptionPane.YES_OPTION) {
            String sql = "DELETE FROM mahasiswa WHERE nim = ?";
            PreparedStatement pst = conn.prepareStatement(sql);
            
            // Set NIM yang akan dihapus
            pst.setString(1, nim);
            
            pst.executeUpdate(); // Jalankan perintah hapus
            
            JOptionPane.showMessageDialog(this, "Data Berhasil Dihapus!");
            
            tampilkanData(); // Refresh tabel
            bersihkanForm(); // Kosongkan form
        }
        
    } catch (HeadlessException | SQLException e) {
        JOptionPane.showMessageDialog(this, "Gagal Hapus: " + e.getMessage());
    }
    }//GEN-LAST:event_BThapusActionPerformed

