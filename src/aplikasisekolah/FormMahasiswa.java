    private void BTeditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTeditActionPerformed
        // TODO add your handling code here:
        try {
        String nimLama = TFnim.getText(); // NIM diambil dari Text Field yang dinonaktifkan
        String namaBaru = TFnama.getText();
        String alamatBaru = TFalamat.getText();
        String kotaBaru = TFkota.getText();
        
        if (nimLama.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pilih data yang akan diedit dari tabel.", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String sql = "UPDATE mahasiswa SET nama = ?, alamat = ?, kota = ? WHERE nim = ?";
        PreparedStatement pst = conn.prepareStatement(sql);
        
        // Set nilai baru
        pst.setString(1, namaBaru);
        pst.setString(2, alamatBaru);
        pst.setString(3, kotaBaru);
        // Set kondisi WHERE (berdasarkan NIM lama)
        pst.setString(4, nimLama);
        
        pst.executeUpdate(); // Jalankan perintah update
        
        JOptionPane.showMessageDialog(this, "Data Berhasil Diperbarui!");
        
        tampilkanData(); // Refresh tabel
        bersihkanForm(); // Kosongkan form
        
    } catch (HeadlessException | SQLException e) {
        JOptionPane.showMessageDialog(this, "Gagal Edit: " + e.getMessage());
    }
    }//GEN-LAST:event_BTeditActionPerformed

    private void BTcetakpdfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTcetakpdfActionPerformed
        // TODO add your handling code here:
        // Tentukan lokasi penyimpanan file
    String path = "";
    JFileChooser j = new JFileChooser();
    j.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
    
    int result = j.showSaveDialog(this);

    if (result == JFileChooser.APPROVE_OPTION) {
        path = j.getSelectedFile().getPath();
    } else {
        return; // Batal jika pengguna tidak memilih folder
    }

    Document doc = new Document();
    
    try {
        // Tentukan output file
        PdfWriter.getInstance(doc, new FileOutputStream(path + "\\LaporanMahasiswa.pdf"));
        doc.open();

        // **KOREKSI FONT DI SINI**
        
        // 1. Definisikan BaseFont (HELVETICA)
        BaseFont bf = BaseFont.createFont(BaseFont.HELVETICA, BaseFont.WINANSI, BaseFont.EMBEDDED);
        
        // 2. Buat objek Font yang menggunakan BaseFont
        Font titleFont = new Font(bf, 16, Font.BOLD);
        
        // 3. Gunakan Font object untuk Judul
        Paragraph title = new Paragraph("LAPORAN DATA MAHASISWA", titleFont);
        title.setAlignment(Element.ALIGN_CENTER);
        doc.add(title);
        doc.add(new Paragraph("\n"));

        // 4. Buat Tabel (4 kolom)
        PdfPTable tbl = new PdfPTable(4); 
        tbl.setWidthPercentage(100);
        
        // Set Font untuk Header Tabel (bisa menggunakan Font object baru)
        Font headerFont = new Font(bf, 10, Font.BOLD, BaseColor.WHITE); 
        
        // Tambah Header Kolom
        com.itextpdf.text.pdf.PdfPCell cell; // Gunakan PdfPCell untuk kustomisasi
        
        cell = new com.itextpdf.text.pdf.PdfPCell(new Paragraph("NIM", headerFont));
        cell.setBackgroundColor(BaseColor.DARK_GRAY);
        tbl.addCell(cell);
        
        cell = new com.itextpdf.text.pdf.PdfPCell(new Paragraph("Nama", headerFont));
        cell.setBackgroundColor(BaseColor.DARK_GRAY);
        tbl.addCell(cell);

        cell = new com.itextpdf.text.pdf.PdfPCell(new Paragraph("Alamat", headerFont));
        cell.setBackgroundColor(BaseColor.DARK_GRAY);
        tbl.addCell(cell);

        cell = new com.itextpdf.text.pdf.PdfPCell(new Paragraph("Kota", headerFont));
        cell.setBackgroundColor(BaseColor.DARK_GRAY);
        tbl.addCell(cell);


        // 5. Ambil Data dari Database dan isi tabel PDF
        String sql = "SELECT * FROM mahasiswa";
        Statement st = conn.createStatement();
        ResultSet rs = st.executeQuery(sql);

        while (rs.next()) {
            tbl.addCell(rs.getString("nim"));
            tbl.addCell(rs.getString("nama"));
            tbl.addCell(rs.getString("alamat"));
            tbl.addCell(rs.getString("kota"));
        }

        // 6. Tambahkan tabel ke dokumen
        doc.add(tbl);
        
        JOptionPane.showMessageDialog(null, "Laporan PDF berhasil dibuat di:\n" + path + "\\LaporanMahasiswa.pdf", "Cetak Berhasil", JOptionPane.INFORMATION_MESSAGE);

    } catch (FileNotFoundException | DocumentException | SQLException ex) {
        JOptionPane.showMessageDialog(null, "Gagal Mencetak PDF: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    } catch (java.io.IOException ex) {
        JOptionPane.showMessageDialog(null, "Gagal Mencetak PDF: File/BaseFont Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    } finally {
        // 7. Tutup dokumen
        if (doc != null && doc.isOpen()) {
            doc.close();
        }
    }
    }//GEN-LAST:event_BTcetakpdfActionPerformed

